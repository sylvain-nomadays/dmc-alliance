interface AdminSidebarProps {
  isAdmin?: boolean;
  isPartner?: boolean;
  partnerName?: string;
}

export function AdminSidebar({ isAdmin = false, isPartner = false, partnerName }: AdminSidebarProps) {
  const pathname = usePathname();

  // Filter navigation items based on role
  const visibleNavigation = navigation.filter((item) => {
    // Admin sees everything except partner-only items
    if (isAdmin) return !item.partnerOnly;
    // Partners see items that are not admin-only (including partner-only items)
    if (isPartner) return !item.adminOnly;
    return false;
  });

  return (
    <div className="hidden lg:fixed lg:inset-y-0 lg:flex lg:w-64 lg:flex-col">
      <div className="flex min-h-0 flex-1 flex-col bg-deep-blue-900">
        {/* Logo */}
        <div className="flex h-16 flex-shrink-0 items-center px-4 border-b border-deep-blue-800">
          <Link href="/admin" className="flex items-center gap-2">
            <div className="w-8 h-8 bg-terracotta-500 rounded-lg flex items-center justify-center">
              <span className="text-white font-bold text-sm">DA</span>
            </div>
            <span className="text-white font-heading text-lg">
              {isAdmin ? 'Admin' : 'Espace Partenaire'}
            </span>
          </Link>
        </div>

        {/* Partner info banner */}
        {isPartner && partnerName && (
          <div className="px-4 py-3 bg-deep-blue-800/50 border-b border-deep-blue-800">
            <p className="text-xs text-deep-blue-300 uppercase tracking-wider mb-1">
              Connecté en tant que
            </p>
            <p className="text-white font-medium truncate">{partnerName}</p>
          </div>
        )}

        {/* Navigation */}
        <div className="flex flex-1 flex-col overflow-y-auto pt-5 pb-4">
          <nav className="flex-1 space-y-1 px-3">
            {visibleNavigation.map((item) => {
              const isActive =
                pathname === item.href ||
                (item.href !== '/admin' && pathname.startsWith(item.href));

              // Use partner-specific name if available and user is partner
              const displayName = isPartner && item.partnerName ? item.partnerName : item.name;

              return (
                <Link
                  key={item.name}
                  href={item.href}
                  className={cn(
                    'flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors',
                    isActive
                      ? 'bg-deep-blue-800 text-white'
                      : 'text-deep-blue-200 hover:bg-deep-blue-800 hover:text-white'
                  )}
                >
                  {item.icon}
                  {displayName}
                </Link>
              );
            })}
          </nav>
        </div>

        {/* Help section for partners */}
        {isPartner && (
          <div className="flex-shrink-0 border-t border-deep-blue-800 p-4">
            <div className="bg-deep-blue-800/50 rounded-lg p-3">
              <p className="text-xs text-deep-blue-300 mb-2">
                Besoin d&apos;aide ?
              </p>
              <a
                href="mailto:support@dmcalliance.com"
                className="text-sm text-terracotta-400 hover:text-terracotta-300 transition-colors"
              >
                Contacter le support
              </a>
            </div>
          </div>
        )}

        {/* Back to site */}
        <div className="flex-shrink-0 border-t border-deep-blue-800 p-4">
          <Link
            href="/"
            className="flex items-center gap-2 text-deep-blue-300 hover:text-white transition-colors text-sm"
          >
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M10 19l-7-7m0 0l7-7m-7 7h18" />
            </svg>
            Retour au site
          </Link>
        </div>
      </div>
    </div>
  );
}
