import { createServerClient } from '@supabase/ssr';
import { createClient as createSupabaseClient } from '@supabase/supabase-js';
import { cookies } from 'next/headers';
import type { Database } from '@/types/database';

export async function createClient() {
  const cookieStore = await cookies();

  return createServerClient<Database>(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll();
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            );
          } catch {
            // The `setAll` method was called from a Server Component.
            // This can be ignored if you have middleware refreshing
            // user sessions.
          }
        },
      },
    }
  );
}

/**
 * Client statique pour les routes générées statiquement (sans cookies)
 * À utiliser pour les données publiques qui ne dépendent pas de l'utilisateur
 */
let staticClientInstance: ReturnType<typeof createSupabaseClient<Database>> | null = null;

export function createStaticClient() {
  if (!staticClientInstance) {
    const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
    const key = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;

    if (!url || !key) {
      console.error('[createStaticClient] Missing env vars:', { url: !!url, key: !!key });
      throw new Error('Supabase URL or Key not configured');
    }

    staticClientInstance = createSupabaseClient<Database>(url, key);
  }
  return staticClientInstance;
}

